t = (3, 23, 12, 54, 23)
print(t.count(23))  # Count the number of occurrences of 23 in the tuple
print(t.index(23))  # Find the index of the first occurrence of 23 in the tuple
print(t.index(23, 1))  # Find the index of the first occurrence of 23 in the tuple starting from index 1
print(t.count(23))  # Count the number of occurrences of 23 in the tuple starting from index 1